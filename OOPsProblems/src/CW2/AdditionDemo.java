package CW2;

public class AdditionDemo {

	int num1;
	int num2;
	int result;
	
	public void add()
	{
		result=num1+num2;
		System.out.println("addition="+result);
	}
	public void sub()
	{
		result=num1-num2;
		System.out.println("sub="+result);
	}
	public void multi()
	{
		result=num1*num2;
		System.out.println("multi="+result);
	}
	public void div () {
		result=num1/num2;
		System.out.println("div="+result);
	}
}
