package sept24;

public class AdditionDemo {

	
	int num1;
	int num2;
	int result;
	
	public void add()
	{
		result=num1+num2;
		System.out.println("Addition is: "+result);
	}
	
	public void sub()
	{
		result=num1-num2;
		System.out.println("Substraction is: "+result);
	}
	public void mul()
	{
		result=num1*num2;
		System.out.println("Multiplication is: "+result);
	}
	public void div()
	{
		result=num1/num2;
		System.out.println("Dividation is: "+result);
	}
}
